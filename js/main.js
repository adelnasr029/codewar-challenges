const memoize = (fn) => {
  let cache = {};
  return (...arg) => {
      let n = arg[0];
      if(n in cache ){
        console.log('Fetching from cach', n)
        return cache[n]
      } else{
        console.log('Caculating result', n)
        let result = fn(n);
        cache[n] = result;
        return result;
      }
  }
}
const factorial = memoize(
  (x) => {
    if(x === 0){
      return 1
    } else {
      return x * factorial(x-1)
    }
  }
)

